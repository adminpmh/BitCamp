package ex;

public class DivOp {

	public static void main(String[] args) {
		
		int num1 = 7;
		int num2 = 3;
		
		//정수의 나누기
		System.out.println("7 / 3 ="+num1/num2);
		System.out.println("7 / 3f ="+num1/3f);
		
		//실수의 연산
		System.out.println("7f / 3f ="+7f/3f);
		System.out.println("7 / 3 ="+(float)num1/num2);
		

	}

}
